package entity;

public class DatePrint {
	
	private String dateFrom;
	private String dateTo;
	
	/* ****** constructor ****** */
	public DatePrint(){
		
	}
	
	public DatePrint(String dateFrom, String dateTo){
		super();
		this.dateFrom = dateFrom;
		this.dateTo = dateTo;
	}
	
	/* ****** method ****** */
	public String getDateFrom(){
		return dateFrom;
	}
	
	public String getDateTo(){
		return dateTo;
	}
}
