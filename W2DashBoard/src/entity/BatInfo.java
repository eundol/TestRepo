package entity;

public class BatInfo {

	private String build;
	private String date;
	private String result;
	private int pass;
	private int total;
	private int passRate;
	private int fail;
	
	public BatInfo(String build, String date, String result, int pass,
			int total, int passRate) {
		super();
		this.build = build;
		this.date = date;
		this.result = result;
		this.pass = pass;
		this.total = total;
		this.passRate = passRate;
		this.fail = total - pass;
	}
	
	public BatInfo() {
		super();
	}
	
	
	public int getFail() {
		return fail;
	}

	public void setFail(int fail) {
		this.fail = fail;
	}

	public String getBuild() {
		return build;
	}
	public void setBuild(String build) {
		this.build = build;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public int getPass() {
		return pass;
	}
	public void setPass(int pass) {
		this.pass = pass;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getPassRate() {
		return passRate;
	}
	public void setPassRate(int passRate) {
		this.passRate = passRate;
	}
}
