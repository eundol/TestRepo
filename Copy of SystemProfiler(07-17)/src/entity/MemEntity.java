package entity;

public class MemEntity {
	
	private String id;
	private int sequence;
	private int total;
	private int used;
	private int free;
	private int hsared;
	private int buffers;
	private int cached;
	
	public MemEntity(String id, int sequence, int total, int used, int free,
			int hsared, int buffers, int cached) {
		super();
		this.id = id;
		this.sequence = sequence;
		this.total = total;
		this.used = used;
		this.free = free;
		this.hsared = hsared;
		this.buffers = buffers;
		this.cached = cached;
	}
	
	public MemEntity() {
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getUsed() {
		return used;
	}
	public void setUsed(int used) {
		this.used = used;
	}
	public int getFree() {
		return free;
	}
	public void setFree(int free) {
		this.free = free;
	}
	public int getHsared() {
		return hsared;
	}
	public void setHsared(int hsared) {
		this.hsared = hsared;
	}
	public int getBuffers() {
		return buffers;
	}
	public void setBuffers(int buffers) {
		this.buffers = buffers;
	}
	public int getCached() {
		return cached;
	}
	public void setCached(int cached) {
		this.cached = cached;
	}

}
