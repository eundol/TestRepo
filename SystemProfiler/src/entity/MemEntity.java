package entity;

public class MemEntity {
	
	private String id;
	private long sequence;
	private long total;
	private long used;
	private long free;
	private long hsared;
	private long buffers;
	private long cached;
	
	public MemEntity(String id, long sequence, long total, long used, long free,
			long hsared, long buffers, long cached) {
		super();
		this.id = id;
		this.sequence = sequence;
		this.total = total;
		this.used = used;
		this.free = free;
		this.hsared = hsared;
		this.buffers = buffers;
		this.cached = cached;
	}
	
	public MemEntity() {
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public long getSequence() {
		return sequence;
	}
	public void setSequence(long sequence) {
		this.sequence = sequence;
	}
	public long getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public long getUsed() {
		return used;
	}
	public void setUsed(int used) {
		this.used = used;
	}
	public long getFree() {
		return free;
	}
	public void setFree(int free) {
		this.free = free;
	}
	public long getHsared() {
		return hsared;
	}
	public void setHsared(int hsared) {
		this.hsared = hsared;
	}
	public long getBuffers() {
		return buffers;
	}
	public void setBuffers(int buffers) {
		this.buffers = buffers;
	}
	public long getCached() {
		return cached;
	}
	public void setCached(int cached) {
		this.cached = cached;
	}

}
