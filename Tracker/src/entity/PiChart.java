package entity;

public class PiChart {

	private int rowCount;
	private int rsGetInt1;
	private int rsGetInt2;
	private String columnName1;
	private String columnName2;
	
	public PiChart(int rowCount, int rsGetInt1, int rsGetInt2, String columnName1, String columnName2) {
		super();
		this.rowCount = rowCount;
		this.rsGetInt1 = rsGetInt1;
		this.rsGetInt2 = rsGetInt2;
		this.columnName1 = columnName1;
		this.columnName2 = columnName2;
	}
	public PiChart(){
		
	}
	
	/* ****** rowCount ****** */
	public int getRowCount(){
		return rowCount;
	}
	
	public void setRowCount(int rowCount){
		this.rowCount = rowCount;
	}
	
	/* ****** rsGetInt1 ****** */
	public int getRsGetInt1(){
		return rsGetInt1;
	}
	
	public void setRsGetInt1(int rsGetInt1){
		this.rsGetInt1 = rsGetInt1;
	}
	
	/* ****** rsGetInt2 ****** */
	public int getRsGetInt2(){
		return rsGetInt2;
	}
	
	public void setRsGetInt2(int rsGetInt2){
		this.rsGetInt2 = rsGetInt2;
	}
	
	/* ****** columnName1 ****** */
	public String getColumnName1(){
		return columnName1;
	}
	
	public void setColumnName1(String columnName1){
		this.columnName1 = columnName1;
	}
	
	/* ****** columnName2 ****** */
	public String getColumnName2(){
		return columnName2;
	}
	
	public void setColumnName2(String columnName2){
		this.columnName2 = columnName2;
	}
}

